package Atividades;

public class Pilot {
	
	String name;
	int age;
	String nationality;
	int number_victories;
	int position;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getNationality() {
		return nationality;
	}
	public void setNationality(String nationality) {
		this.nationality = nationality;
	}
	public int getNumber_victories() {
		return number_victories;
	}
	public void setNumber_victories(int number_victories) {
		this.number_victories = number_victories;
	}
	public int getPosition() {
		return position;
	}
	public void setPosition(int position) {
		this.position = position;
	}
	

}
